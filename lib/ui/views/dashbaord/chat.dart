class Chat extends StatelessWidget {
  final String chaterId;
  Chat({Key key, this.chaterId}) : super(key: key);
  final GlobalKey<ScaffoldState> _drawerKey = new GlobalKey<ScaffoldState>();

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      key: _drawerKey,
      drawer: PopDrawer(),
      appBar: PopAppBar(
          title: 'Chat',
          callback: () {
            _drawerKey.currentState.openDrawer();
          }),
      body: ChatScreen(chaterId: chaterId),
    );
  }
}

class ChatScreen extends StatefulWidget {
  final String chaterId;
  ChatScreen({Key key, this.chaterId}) : super(key: key);

  @override
  _ChatScreenState createState() => _ChatScreenState(chaterId: chaterId);
}

class _ChatScreenState extends State<ChatScreen> {
  _ChatScreenState({Key key, this.chaterId});
  String chaterId;
  String userId;

  var listMessage;
  String groupChatId;
  SharedPreferences prefs;

  File imageFile;
  bool loading;
  bool isSticker;
  String imageUrl;

  final TextEditingController textEditingController = TextEditingController();
  final ScrollController listScrollController = ScrollController();
  final FocusNode focusNode = FocusNode();

  @override
  void initState() {
    super.initState();
    focusNode.addListener(onFocusChange);

    groupChatId = '';

    loading = false;
    isSticker = false;
    imageUrl = '';

    readLocal();
  }

  void onFocusChange() {
    if (focusNode.hasFocus) {
      setState(() {
        isSticker = false;
      });
    }
  }

  readLocal() async {
    prefs = await SharedPreferences.getInstance();
    userId = prefs.getString('userId') ?? '';
    print('userId => $userId');
    if (userId.hashCode <= chaterId.hashCode) {
      groupChatId = '$userId-$chaterId';
    } else {
      groupChatId = '$chaterId-$userId';
    }

    Firestore.instance
        .collection('users')
        .document(userId)
        .updateData({'chattingWith': chaterId});

    setState(() {});
  }

  Future getImage() async {
    imageFile = await ImagePicker.pickImage(source: ImageSource.gallery);

    if (imageFile != null) {
      setState(() {
        loading = true;
      });
      uploadFile();
    }
  }

  void getSticker() {
    focusNode.unfocus();
    setState(() {
      isSticker = !isSticker;
    });
  }

  Future uploadFile() async {
    String fileName = DateTime.now().millisecondsSinceEpoch.toString();
    StorageReference reference = FirebaseStorage.instance.ref().child(fileName);
    StorageUploadTask uploadTask = reference.putFile(imageFile);
    StorageTaskSnapshot storageTaskSnapshot = await uploadTask.onComplete;
    storageTaskSnapshot.ref.getDownloadURL().then((downloadUrl) {
      imageUrl = downloadUrl;
      setState(() {
        loading = false;
        onSendMessage(imageUrl, 1);
      });
    }, onError: (error) {
      setState(() {
        loading = false;
      });
      //Fluttertoast.showToast(msg: 'This file is not an image');
    });
  }

  void onSendMessage(String content, int type) {
    // type: 0 = text, 1 = image, 2 = sticker
    if (content.trim() != '') {
      textEditingController.clear();

      var documentReference = Firestore.instance
          .collection('messages')
          .document(groupChatId)
          .collection(groupChatId)
          .document(DateTime.now().millisecondsSinceEpoch.toString());

      Firestore.instance.runTransaction((transaction) async {
        await transaction.set(
          documentReference,
          {
            'idFrom': userId,
            'idTo': chaterId,
            'timestamp': DateTime.now().millisecondsSinceEpoch.toString(),
            'content': content,
            'type': type
          },
        );
      });
      listScrollController.animateTo(0.0,
          duration: Duration(milliseconds: 300), curve: Curves.easeOut);
    } else {
      //Fluttertoast.showToast(msg: 'Nothing to send');
    }
  }

  Widget buildItem(int index, DocumentSnapshot document) {
    if (document['idFrom'] == userId) {
      // Right (my message)
      return Row(
        children: <Widget>[
          document['type'] == 0
              // Text
